package Modelo;

public class Pedido {
	
	int pPlato;	
	int sPlato;	
	int bebida;	
	int postre;	
	int infusion;

	public Pedido() {
		
		
	}

	public int getpPlato() {
		return pPlato;
	}

	public void setpPlato(int pPlato) {
		this.pPlato = pPlato;
	}

	public int getsPlato() {
		return sPlato;
	}

	public void setsPlato(int sPlato) {
		this.sPlato = sPlato;
	}

	public int getBebida() {
		return bebida;
	}

	public void setBebida(int bebida) {
		this.bebida = bebida;
	}

	public int getPostre() {
		return postre;
	}

	public void setPostre(int postre) {
		this.postre = postre;
	}

	public int getInfusion() {
		return infusion;
	}

	public void setInfusion(int infusion) {
		this.infusion = infusion;
	}
	
	
	

}
